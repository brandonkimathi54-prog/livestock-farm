drop policy if exists "Public read livestock photos" on storage.objects;
create policy "Public read livestock photos"
  on storage.objects
  for select
  to anon, authenticated
  using (bucket_id = 'cow photos');

drop policy if exists "Public upload livestock photos" on storage.objects;
