-- Health logs table for tracking livestock health conditions and treatments
create table if not exists public.health_logs (
  id bigint generated by default as identity primary key,
  livestock_id bigint not null references public.livestock(id),
  condition text not null,
  treatment text,
  cost numeric(10,2) default 0 check (cost >= 0),
  created_at timestamptz not null default now()
);

create index if not exists health_logs_created_at_idx
  on public.health_logs (created_at desc);

create index if not exists health_logs_livestock_id_idx
  on public.health_logs (livestock_id);

alter table public.health_logs enable row level security;

