-- Run this script in Supabase SQL Editor.
-- It creates the livestock table and configures public read/insert access.

create table if not exists public.livestock (
  id bigint generated by default as identity primary key,
  name text not null check (char_length(name) >= 2),
  breed text not null check (char_length(breed) >= 2),
  price_ksh numeric(12,2) not null check (price_ksh >= 0),
  health_status text not null default 'Healthy',
  image_url text,
  created_at timestamptz not null default now()
);

alter table public.livestock
  add column if not exists price_ksh numeric(12,2);
do $$
begin
  if exists (
    select 1
    from information_schema.columns
    where table_schema = 'public'
      and table_name = 'livestock'
      and column_name = 'price'
  ) then
    execute '
      update public.livestock
      set price_ksh = price
      where price_ksh is null and price is not null
    ';
  end if;
end $$;
alter table public.livestock
  alter column price_ksh set not null;

alter table public.livestock
  add column if not exists health_status text not null default 'Healthy';
alter table public.livestock
  add column if not exists image_url text;
alter table public.livestock
  add column if not exists weight numeric(8,2);
alter table public.livestock
  add column if not exists age numeric(5,1);
alter table public.livestock
  add column if not exists status text default 'Active' check (status in ('Active', 'For Sale', 'Sold', 'Retired'));
alter table public.livestock
  add column if not exists sale_price numeric(12,2) check (sale_price >= 0);

create index if not exists livestock_created_at_idx
  on public.livestock (created_at desc);

alter table public.livestock enable row level security;

drop policy if exists "Public read livestock" on public.livestock;
create policy "Public read livestock"
  on public.livestock
  for select
  to anon, authenticated
  using (true);

drop policy if exists "Public insert livestock" on public.livestock;
create policy "Public insert livestock"
  on public.livestock
  for insert
  to anon, authenticated
  with check (true);

drop policy if exists "Authenticated update livestock" on public.livestock;
create policy "Authenticated update livestock"
  on public.livestock
  for update
  to authenticated
  using (true)
  with check (true);

drop policy if exists "Authenticated delete livestock" on public.livestock;
create policy "Authenticated delete livestock"
  on public.livestock
  for delete
  to authenticated
  using (true);

-- Storage bucket for livestock photos
insert into storage.buckets (id, name, public)
values ('cow photos', 'cow photos', true)
